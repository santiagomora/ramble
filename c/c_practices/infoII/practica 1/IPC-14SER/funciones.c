/**
    \fn     enviar
    \brief  Envia un numero aleatorio al socket
    \author Luis Mora
    \date   06-05-2020
    \return 0 si hay exito, 1 si hay un error;
*/
int enviar( int sock ){
    srand( time( NULL ) );
    int rnd = rand()%6+1;
    escribefd( sock,&rnd,sizeof( int ) );
    printf("ENVIADO:%d\n",rnd);
    close( sock );
    return 0;
}

