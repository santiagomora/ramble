/**
    \fn  crearHijo
    \brief  Crea una instancia hijo del proceso que termina
            a los 5 segundos
    \author Luis Mora
    \date 02-05-2020
    \param [in] void
    \param [int] 0
    \return int 0;
*/
int crearHijo(){
    int pid = fork();
    if ( pid ){
        printf( "Padre: PID %d.\n",getpid() );
        esperarHijos();
    } else
        imprimirHijo( CHILDLIVE );
    return 0;
}
