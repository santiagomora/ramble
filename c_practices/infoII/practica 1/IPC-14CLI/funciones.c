/**
    \fn     recibir
    \brief  recibe informacion del servidor
    \author Luis Mora
    \date   06-05-2020
    \return 0 si hay exito, 1 si hay un error;
*/
int recibir( int sock ){
    int buf;
    leefd( sock,&buf,sizeof(int) );
    printf("RECIBIDO:%d.\n",buf);
    return 0;
}

