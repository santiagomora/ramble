/**
    \fn     enviar
    \brief  enviar el mensaje escrito por linea de comando
    \author Luis Mora
    \date 02-05-2020
    \param [in] char* msj el mensaje escrito por linea de comando
    \return 0 si hay exito, 1 si hay un error;
*/
int enviar( char* msj ){
    int fifofd = abrefifo( FIFOPATH,O_WRONLY );
    escribefd( fifofd,msj,strlen( msj )*sizeof( char ) );
    printf( "Mensaje enviado:\n\t%s\n",msj );
    exit(0);
}
